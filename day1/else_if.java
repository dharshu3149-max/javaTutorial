package day1;

public class else_if {
    public static void main(String[] args){
        int time = 14;

        if (time < 12) {
            System.out.println("Good morning");
        } else if (time < 18) {
            System.out.println("Good afternoon");
        } else {
            System.out.println("Good evening");
        }

    }
}
